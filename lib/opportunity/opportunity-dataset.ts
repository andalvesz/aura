export function getNicheById(id: string): DigitalNiche | undefined {
  return DIGITAL_NICHES.find((n) => n.id === id);
}

export function getNicheByName(name: string): DigitalNiche | undefined {
  const normalized = name.toLowerCase().trim();
  return DIGITAL_NICHES.find(
    (n) =>
      n.name.toLowerCase() === normalized ||
      n.id === normalized ||
      normalized.includes(n.name.toLowerCase()) ||
      n.name.toLowerCase().includes(normalized)
  );
}
